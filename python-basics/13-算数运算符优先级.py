if __name__ == "__main__":
    print(1 + 2 * 3)

    # 先乘除 后加减

    # //运算  优先级

    print(2 + 11 // 3)  # 优先级高于+ -
    # // 与 * / 平级
    print(2 * 11 // 3)
    print(11 // 3 * 2)

    # % 也和 * / 平级
    print(2 + 11 % 3)  # 优先级高于+ -
    print(2 * 11 % 3)
    print(11 % 3 * 2)

    # ** 优先级  高于  * /
    print(2 * 3 ** 2)

    # 结论算数运算符优先级: + - < * / // % < **
    # 如果忘记了也没关系使用()提高运算符优先级即可