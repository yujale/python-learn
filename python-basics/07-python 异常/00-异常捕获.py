# 异常捕获:通过代码将可能出现异常的文件放入try中,然后如果出现异常就执行except中的命令
"""
格式:
try:
    可能出现异常的代码
except:
    如果出现了异常,就执行其中的代码
"""

# 需求:读取文件,如果文件不存在,则以写入方式打开
# 如果我们try中的代码出现了异常,则执行except中的命令
# 如果我们try中的代码没有出现异常,则不会执行
try:
    file = open('test.py', 'r')
except:
    file = open('test.py', 'w')

# 在正常的Python开发中基本每个函数中都要出现一次异常捕获
# 代码健壮性:代码抵御异常的能力
